    private void loadDias(){
        dias = DiaAsistenciaData.getDiasAsistenciaHoy();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for(DiaAsistencia dia : dias){
            modelo.addElement(dia.getDescripcion());
        }
        listaDias.setModel(modelo);
        diaActual = dias.get(0);
        DiaAsistenciaData.setDiaTrabajo(diaActual);
    }
    
    private Action onEnter(){
        return new AbstractAction() {

            @Override
            public void actionPerformed(ActionEvent e) {
                if(txtCarnet.isEditValid()){
                    try {
                        txtCarnet.getFormatter().valueToString(txtCarnet.getText());
                        accionActual.exec();
                    } catch (ParseException ex) {
                        txtCarnet.setText("");
                        txtCarnet.requestFocusInWindow();
                    }
                }
            }
        };
    }

