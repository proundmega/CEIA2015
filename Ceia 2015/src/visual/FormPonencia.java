    private void txtIntroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIntroKeyReleased
        if(evt.getKeyCode() != KeyEvent.VK_ENTER){
            String texto = txtCarnet.getText();
            if(!textoAnterior.equals(texto)){
                accionActual = getActionVerificar();
                textoAnterior = texto;
                txtApellidos.setText("");
                txtNombre.setText("");
                txtCarrera.setText("");
                output.setText("");
                btnRegistrar.setText("Verificar si esta registrado");
            }
        }
    }//GEN-LAST:event_txtIntroKeyReleased

    private void txtCarnetFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCarnetFocusLost
        if(txtCarnet.isEditValid()){
            try {
                txtCarnet.getFormatter().valueToString(txtCarnet.getText());
            } catch (ParseException ex) {
                txtCarnet.setText("");
                txtCarnet.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_txtCarnetFocusLost

    private void listaDiasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_listaDiasItemStateChanged
        String descripcion = listaDias.getSelectedItem().toString();
        DiaAsistencia diaSeleccionado = dias.stream()
                .filter(dia -> dia.getDescripcion().equals(descripcion))
                .findFirst().get();
        diaActual = diaSeleccionado;
        DiaAsistenciaData.setDiaTrabajo(diaActual);
    }//GEN-LAST:event_listaDiasItemStateChanged
        private ActionButtom getActionVerificar(){
        
            return new ActionButtom() {

            @Override
            public void exec() {
                if(txtCarnet.isEditValid()){
                    if(existeAlumnoIngresado()){
                        cambiarAction();
                        mostrarAlumno();
                    }
                    else{
                        procesarIngresoNuevoAlumno();
                    }
                }
            }
            
            private boolean existeAlumnoIngresado(){
                return AlumnoData.isAlumnoRegistrado(txtCarnet.getText());
            }
            
            private void cambiarAction(){
                alumno = AlumnoData.getAlumno(txtCarnet.getText());
                accionActual = getActionInsertar();
                btnRegistrar.setText("Registrar alumno");
            }
            
            private void procesarIngresoNuevoAlumno(){
                FormNuevoAlumno dialog = new FormNuevoAlumno(null, txtCarnet.getText());
                dialog.setVisible(true);
                try{
                    alumno = dialog.getAlumnoIngresado();
                    mostrarAlumno();
                    DiaAsistenciaData.agregarAlumnoADiaActual(txtCarnet.getText());
                    limpiar();
                    printExito();
                }
                catch(IndexOutOfBoundsException ex){
                    output.setText(ex.getMessage());
                }
                catch(IllegalArgumentException ex){
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                    limpiar();
                }
                catch(UnsupportedOperationException ex){
                    JOptionPane.showMessageDialog(null, "Esta persona ya se ha registrado este dia");
                    limpiar();
                }
                //Excepcion arrojada por el dialog para saber si no se agrego alumno nuevo
                catch(IllegalStateException ex){}
            }
            
        };
    }
    
    private ActionButtom getActionInsertar(){
        return new ActionButtom() {

            @Override
            public void exec() {
                try{
                    DiaAsistenciaData.agregarAlumnoADiaActual(txtCarnet.getText());
                    limpiar();
                    cambiarAction();
                    printExito();
                }
                catch(IndexOutOfBoundsException ex){
                    output.setText(ex.getMessage());
                }
                catch(IllegalArgumentException ex){
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                    limpiar();
                }
                catch(UnsupportedOperationException ex){
                    JOptionPane.showMessageDialog(null, "Esta persona ya se ha registrado este dia");
                    limpiar();
                }
            }
            
            private void cambiarAction(){
                accionActual = getActionVerificar();
                btnRegistrar.setText("Verificar si esta registrado");
            }
        };
    }
    
    private void mostrarAlumno(){
        txtApellidos.setText(alumno.getApellidos());
        txtNombre.setText(alumno.getNombres());
        txtCarrera.setText(alumno.getCarrera().getCarrera());
    }
    
    private void limpiar(){
        output.setText("");
        txtCarnet.setValue("");
        txtApellidos.setText("");
        txtNombre.setText("");
        txtCarrera.setText("");
        txtCarnet.requestFocusInWindow();
    }
    
    private void printExito(){
        output.setText("Alumno registrado con éxito");
    }
    
